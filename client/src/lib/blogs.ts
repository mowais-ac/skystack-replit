// Helper functions for blog management
export function getBlogBySlug(slug: string): BlogPost | undefined {
  return blogs.find(blog => blog.slug === slug && blog.status === "published");
}

export function getBlogsByCategory(category: string): BlogPost[] {
  return blogs.filter(blog => blog.category === category && blog.status === "published");
}

export function getAllPublishedBlogs(): BlogPost[] {
  return blogs.filter(blog => blog.status === "published").sort((a, b) => 
    new Date(b.publishedAt).getTime() - new Date(a.publishedAt).getTime()
  );
}

export function getRecentBlogs(limit: number = 5): BlogPost[] {
  return getAllPublishedBlogs().slice(0, limit);
}

export function getRelatedBlogs(currentBlogId: string, limit: number = 3): BlogPost[] {
  const currentBlog = blogs.find(b => b.id === currentBlogId);
  if (!currentBlog) return [];
  
  return blogs
    .filter(blog => 
      blog.id !== currentBlogId && 
      blog.status === "published" &&
      (blog.category === currentBlog.category || 
       blog.tags.some(tag => currentBlog.tags.includes(tag)))
    )
    .slice(0, limit);
}
